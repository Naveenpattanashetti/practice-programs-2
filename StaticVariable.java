package demo.java;

class Mobile {
	String brand;
	String name;
	int price;

// IN STATIC METHOD WE CAN CALL THE METHOD WITHOUT CREATING A OBJECT  WE CAN CALL IT BY CLASS NAME //

}

public class StaticVariable {

	public static void main(String[] args) {

		Mobile obj = new Mobile();
		obj.brand = "apple";
		obj.name = "apple15";
		obj.price = 1500;
		

	}

}
